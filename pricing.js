function currentLang(){
  return document.documentElement.lang==="es"?"es":"en";
}

function applyStaticTermsTranslations(lang){
  const t=pricingTranslations[lang];
  const gate=document.querySelector("#terms-gate");
  if(gate){
    const strong=gate.querySelector("strong");
    const paragraphs=gate.querySelectorAll("p");
    const link=gate.querySelector("a");
    if(strong) strong.textContent=t.termsRequired;
    if(paragraphs[1]) paragraphs[1].textContent=t.termsInstructions;
    if(link) link.textContent=t.reviewTerms;
  }
  const confirmation=document.querySelector("#booking-policy-consent")?.closest("label")?.querySelector("span");
  if(confirmation) confirmation.textContent=t.termsConfirmation;
}

function setLanguage(lang){
  const safe=pricingTranslations[lang]?lang:"en";
  document.documentElement.lang=safe;
  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const key=el.dataset.i18n;
    if(pricingTranslations[safe][key]!==undefined){
      el.textContent=pricingTranslations[safe][key];
    }
  });
  document.querySelectorAll(".lang-btn").forEach(btn=>{
    btn.setAttribute("aria-pressed",String(btn.dataset.lang===safe));
  });
  localStorage.setItem("domzor-language",safe);
  localStorage.setItem("domzorLanguage",safe);
  document.title=safe==="es"?"DOMZOR | Precios y Reservas":"DOMZOR | Pricing & Booking";
  applyStaticTermsTranslations(safe);
  refreshTermsAcceptance();
  updateCalculator();
}

