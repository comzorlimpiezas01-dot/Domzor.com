function numberValue(element, fallback=0){
  if(!element) return fallback;
  const value=Number(element.value);
  return Number.isFinite(value) ? value : fallback;
}

function calculate(){
  if(!service) return {total:0,deposit:0};
  const key=service.value;
  const base=basePrices[key] ?? basePrices.residential;
  const multiplier=Math.max(1,numberValue(size,1));
  const roomCount=Math.max(0,numberValue(bedrooms,0));
  const bathCount=Math.max(0,numberValue(bathrooms,0));
  const extraCount=Math.max(0,numberValue(extras,0));

  let total=base;

  switch(key){
    case "residential":
      total=base*multiplier+roomCount*22+bathCount*28+extraCount*18;
      break;
    case "commercial":
      total=base*multiplier+roomCount*35+bathCount*35+extraCount*25;
      break;
    case "move":
      total=base*multiplier+roomCount*30+bathCount*35+extraCount*20;
      break;
    case "carpet":
      total=base+Math.max(0,roomCount-1)*70+extraCount*35;
      total*=multiplier;
      break;
    case "windows":
      total=base+extraCount*8;
      total*=multiplier;
      break;
    case "upholstery":
      total=base+extraCount*60;
      total*=multiplier;
      break;
    case "water":
      total=base*multiplier+extraCount*125;
      break;
    case "mold":
      total=base*multiplier+extraCount*150;
      break;
    case "fire":
      total=base*multiplier+extraCount*125;
      break;
    default:
      total=base;
  }

  if(urgent?.checked){
    total+=EMERGENCY_FEE;
  }

  total=Math.max(base,Math.round(total/5)*5);
  const deposit=Math.round(total*0.30*100)/100;
  return {total,deposit};
}

function updateCalculator(){
  if(!service || !totalEl || !depositEl) return;
  const {total,deposit}=calculate();
  totalEl.textContent=`$${total.toLocaleString("en-US",{maximumFractionDigits:2})}`;
  depositEl.textContent=`$${deposit.toLocaleString("en-US",{minimumFractionDigits:Number.isInteger(deposit)?0:2,maximumFractionDigits:2})}`;

  if(whatsapp){
    const lang=currentLang();
    const label=serviceLabels[lang]?.[service.value] || service.value;
    const sizeText=size?.options?.[size.selectedIndex]?.text || "";
    const message=lang==="es"
      ? `Solicitud de estimación DOMZOR\nServicio: ${label}\nEstimación: $${total}\nAnticipo estimado 30%: $${deposit}\nTamaño: ${sizeText}\nHabitaciones/áreas: ${bedrooms?.value||0}\nBaños: ${bathrooms?.value||0}\nExtras: ${extras?.value||0}\nEmergencia (+$250): ${urgent?.checked?"Sí":"No"}\nEntiendo que el precio final requiere confirmación.`
      : `DOMZOR estimate request\nService: ${label}\nEstimate: $${total}\nEstimated 30% deposit: $${deposit}\nSize: ${sizeText}\nBedrooms/work areas: ${bedrooms?.value||0}\nBathrooms: ${bathrooms?.value||0}\nExtras: ${extras?.value||0}\nEmergency (+$250): ${urgent?.checked?"Yes":"No"}\nI understand the final price requires confirmation.`;
    whatsapp.href=`https://wa.me/13127784975?text=${encodeURIComponent(message)}`;
  }
}

[service,size,bedrooms,bathrooms,extras,urgent].forEach(el=>{
  if(!el) return;
  el.addEventListener("input",updateCalculator);
  el.addEventListener("change",updateCalculator);
});

const policyCheckbox=document.querySelector("#booking-policy-consent");
const acceptedMessage=document.querySelector("#terms-accepted-message");
let termsAreCurrent=false;

function readTermsAcceptance(){
  try{
    const parsed=JSON.parse(localStorage.getItem("domzorTermsAcceptance")||"null");
    return parsed && parsed.version===TERMS_VERSION && parsed.acceptedTerms===true && parsed.electronicConsent===true ? parsed : null;
  }catch(error){
    return null;
  }
}

function refreshTermsAcceptance(){
  const acceptance=readTermsAcceptance();
  termsAreCurrent=Boolean(acceptance);
  if(policyCheckbox){
    policyCheckbox.disabled=!termsAreCurrent;
    policyCheckbox.checked=termsAreCurrent;
  }
  if(acceptedMessage){
    if(acceptance){
      acceptedMessage.hidden=false;
      const label=pricingTranslations[currentLang()].acceptedBy;
      acceptedMessage.textContent=`${label} ${acceptance.fullName} — ${new Date(acceptance.acceptedAt).toLocaleString(currentLang()==="es"?"es-US":"en-US")}.`;
    }else{
      acceptedMessage.hidden=true;
      acceptedMessage.textContent="";
    }
  }
}

window.addEventListener("pageshow",()=>{
  refreshTermsAcceptance();
  updateCalculator();
});

document.addEventListener("visibilitychange",()=>{
  if(!document.hidden){
    refreshTermsAcceptance();
    updateCalculator();
  }
});

