function currentLang(){return document.documentElement.lang==="es"?"es":"en";}
function setLanguage(lang){
  const safe=pricingTranslations[lang]?lang:"en";
  document.documentElement.lang=safe;
  document.querySelectorAll("[data-i18n]").forEach(el=>{const key=el.dataset.i18n;if(pricingTranslations[safe][key]!==undefined)el.textContent=pricingTranslations[safe][key];});
  document.querySelectorAll(".lang-btn").forEach(btn=>btn.setAttribute("aria-pressed",String(btn.dataset.lang===safe)));
  localStorage.setItem("domzor-language",safe);
  document.title=safe==="es"?"DOMZOR | Precios y Reservas":"DOMZOR | Pricing & Booking";
  updateCalculator();
}
document.querySelectorAll(".lang-btn").forEach(btn=>btn.addEventListener("click",()=>setLanguage(btn.dataset.lang)));
